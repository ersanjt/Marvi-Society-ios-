export type PortalAdminDict = (typeof portalAdmin)["en"];

export function getPortalAdminDict(locale: Locale): PortalAdminDict {
  return (portalAdmin[locale] ?? portalAdmin.en) as PortalAdminDict;
}

export function tReplace(template: string, vars: Record<string, string | number>): string {
  return Object.entries(vars).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template
  );
}
